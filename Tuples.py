var1=("Hello", "World")#Tuple with two elements
print(var1)  # Output: ('Hello', 'World')
print(var1[1])  # Output: World
var2="Hello", "World"  # Tuple without parentheses
print(var2)  # Output: ('Hello', 'World')
print(var2[0])  # Output: Hello
print(var2[1])  # Output: World
var3=()  # Empty tuple
print(var3)  # Output: ()
var4=(1, 2, 3, 4, 5)  # Tuple with integers
print(var4)  # Output: (1, 2, 3, 4
print(var4[-2])  # Output: 4 (negative indexing)
print(var4[-5])  # Output: 1 (negative indexing)
my_tuple = ("c", "a", "t", "s")  # Tuple with characters
print(my_tuple.count("t"))  # Output: 1 (count of 't')
print(my_tuple.index("a"))  # Output: 1 (index of 'a')
print(my_tuple.index("s"))  # Output: 3 (index of 's')
print(my_tuple.index("c"))  # Output: 0 (index of 'c')


